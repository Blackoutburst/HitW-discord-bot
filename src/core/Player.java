package core;

public class Player {
	public int W;
	public int R;
	public int Q;
	public int F;
	public int total;
	public String name;
	public String discord;
	
	public Player(int w, int r, int q, int f, int total, String name, String discord) {
		W = w;
		R = r;
		Q = q;
		F = f;
		this.total = total;
		this.name = name;
		this.discord = discord;
	}
	
	
	
}
